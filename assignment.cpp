#include <iostream>
using namespace std;
int main (){
   int var1, var2 ;
   int tambah, kurang, kali, bagi ;
   var1 = 10; // tipe data : integer
   var2 = 5;
   cout << "Angka 1=" << var1 << endl;
   cout << "Angka 2=" << var2 << endl;
   tambah = var1 + var2 ;
   cout << "Hasil tambah=" << tambah << endl;
   kurang = var1 - var2 ;
   cout << "Hasil pengurangan=" << kurang <<endl;
   kali = var1 * var2;
   cout <<"Hasil perkalian=" << kali <<endl;
   bagi = var1 / var2;
   cout <<"Hasil pembagian=" << bagi <<endl;
}